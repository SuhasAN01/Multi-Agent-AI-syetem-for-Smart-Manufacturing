import 'dotenv/config';
import { createAgentGraph } from "./src/agents/failure/graph.js";
import getMongoClientPromise from "./src/integrations/mongodb/client.js";

async function runDemo() {
  try {
    const alertData = {
      machine_id: "demo-001",
      temperature: 95,
      vibration: 0.9,
      timestamp: new Date().toISOString(),
      anomaly: true,
      description: "Simulated overheating and vibration spike"
    };

    const threadId = "demo-" + Date.now().toString();
    const jobId = "mock-job-12345";

    const message = JSON.stringify(alertData);
    
    console.log(`[Worker Mock] Started processing agent workflow for Job ${jobId}`);

    const client = await getMongoClientPromise();
    const dbName = process.env.DATABASE_NAME;
    const db = client.db(dbName);

    const graph = createAgentGraph(client, dbName);
    console.log("[Worker Mock] Invoking LangGraph...");
    
    const resultState = await graph.invoke(
      { messages: [{ role: "user", content: message }], digitalTwinContext: {} },
      { configurable: { thread_id: threadId } }
    );

    const messages = resultState.messages || [];
    const incidentReport = messages[messages.length - 1]?.content || null;

    if (!incidentReport) {
      throw new Error("No incident report generated by failure agent");
    }

    console.log(`[Worker Mock] Successfully completed Agent workflow. Extracting LLM report...`);
    
    try {
      const insertRes = await db.collection("incident_reports").insertOne({
        alertId: alertData.machine_id,
        report: incidentReport,
        created_at: new Date(),
        job_id: jobId,
      });
      console.log("[Worker Mock] Incident report inserted! ID:", insertRes.insertedId);
    } catch (e) {
      console.error("Insert failed:", e);
    }
    
    // Print verify count
    const count = await db.collection("incident_reports").countDocuments({ job_id: jobId });
    console.log(`\n--- Verification ---`);
    console.log(`Successfully verified ${count} document(s) in incident_reports for demo job!`);
    console.log(`Report Snippet:`, incidentReport.slice(0, 150) + "...\n");

    process.exit(0);
  } catch (error) {
    console.error(error);
    process.exit(1);
  }
}

runDemo();
